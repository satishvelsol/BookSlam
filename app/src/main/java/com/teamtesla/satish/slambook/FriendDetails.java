public class FriendDetails {

    String name;
    String nick_name;
    String dob;
    String mobile;
    String address;
    String best_friend;
    String fav_dish;
    String fav_color;
    String hobbies;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNick_name() {
        return nick_name;
    }

    public void setNick_name(String nick_name) {
        this.nick_name = nick_name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBest_friend() {
        return best_friend;
    }

    public void setBest_friend(String best_friend) {
        this.best_friend = best_friend;
    }

    public String getFav_dish() {
        return fav_dish;
    }

    public void setFav_dish(String fav_dish) {
        this.fav_dish = fav_dish;
    }

    public String getFav_color() {
        return fav_color;
    }

    public void setFav_color(String fav_color) {
        this.fav_color = fav_color;
    }

    public String getHobbies() {
        return hobbies;
    }

    public void setHobbies(String hobbies) {
        this.hobbies = hobbies;
    }



}
